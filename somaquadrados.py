# Ler três valores numéricos inteiros
valor1 = int(input("Digite o primeiro valor inteiro: "))
valor2 = int(input("Digite o segundo valor inteiro: "))
valor3 = int(input("Digite o terceiro valor inteiro: "))

# Calcular a soma dos quadrados dos três valores
soma_quadrados = valor1  + valor2  + valor3

# Armazenar o valor da soma dos quadrados em memória
resultado = soma_quadrados

# Apresentar o resultado
print("A soma dos quadrados dos três valores é:", resultado)
